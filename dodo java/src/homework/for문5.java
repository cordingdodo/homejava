public class for문5 {


	public static void main(String[] args) {
		//0부터 20까지 짝수만 출력해보자.
	for(int a=0;a<=20;a+=2){
		System.out.println(a);
		//하나 더 나머지로
		System.out.println();
		System.out.println("****************");
	for(int i=0;i<=20;i++){
		if(i%2==0){
		System.out.println(i);
	}
	}

}
}
}